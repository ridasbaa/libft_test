int	ft_isalpha(int c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122))
		return (1);
	else
		return (0);
}

int	ft_isdigit(int c)
{
	if (c >= '0' && c <= '9')
		return (1);
	else
		return (0);
}	

int	ft_isalnum(int c)
{
	if (is_digit(c) || is_alpha(c))
		return (1);
	else
		return (0);
}
