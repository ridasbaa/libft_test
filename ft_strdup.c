#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		strlen;
	char	*str;
	int		i;

	strlen = 0;
	if (!s)
		return (NULL);
	while (s[strlen])
		strlen++;
	str = malloc(strlen + 1);
	if (!str)
		return (NULL);
	i = 0;
	while (s[i])
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
